#include "3-calc.h"

/**
 * op_add - addtion
 * @a: first number
 * @b: 2nd number
 *
 * Return: an integer
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substruction
 * @a: first number
 * @b: 2nd number
 *
 * Return: an integer
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - addtion
 * @a: first number
 * @b: 2nd number
 *
 * Return: an integer
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division
 * @a: first number
 * @b: 2nd number
 *
 * Return: an integer
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo
 * @a: first number
 * @b: 2nd number
 *
 * Return: an integer
 */

int op_mod(int a, int b)
{
	return (a % b);
}
